//                         charAt
// Los caracteres de una cadena se indexan de izquierda a derecha. 
// El índice del primer caracter es 0, y el índice del último 
// caracter seria -1. Si el indice que se proporciona está fuera del rango, 
// JavaScript devuelve una cadena vacía.

const nombre = "Gadir"

console.log(nombre.charAt(0))
console.log(nombre.charAt(nombre.length - 1))