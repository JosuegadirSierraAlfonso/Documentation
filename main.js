//                                      Math.ceil
/* The Math.ceil() static method always rounds up and returns the smaller integer greater than or equal to a given number.*/

console.log(Math.ceil(.95));
// Expected output: 1

console.log(Math.ceil(4));
// Expected output: 4

console.log(Math.ceil(7.004));
// Expected output: 8

console.log(Math.ceil(-7.004));
// Expected output: -7
