//                                      Math.floor
/* The Math.floor() static method always rounds down and returns the largest integer less than or equal to a given number.*/


console.log(Math.floor(5.95));
// Expected output: 5

console.log(Math.floor(5.05));
// Expected output: 5

console.log(Math.floor(5));
// Expected output: 5

console.log(Math.floor(-5.05));
// Expected output: -6